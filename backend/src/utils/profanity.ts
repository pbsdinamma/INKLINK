/**
 * Moderation utility.
 * Replaces profane words with asterisks (*), preserving exact word length and boundaries.
 */
export function censorText(text: string): string {
  if (!text) return text;

  // Split by word boundaries to preserve spaces, punctuation, and non-profane words accurately
  return text
    .split(/\b/)
    .map((segment) => {
      const clean = segment.toLowerCase().trim();
      if (PROFANITY_WORDS.has(clean)) {
        return "*".repeat(segment.length);
      }
      return segment;
    })
    .join("");
}
